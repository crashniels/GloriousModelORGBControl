const int VID = 0x258A;
const int PID = 0x0036;
libusb_device_handle* device;

__declspec(dllexport) int DetectDevice()
{
    //printf("Searching for Device: \n");

    libusb_context* ctx;
    libusb_init(&ctx);
    //libusb_set_debug(ctx, 3);

    libusb_device_handle* dev = libusb_open_device_with_vid_pid(ctx, VID, PID);
    
    if (dev)
    {
        //printf("Device Found \n");
        libusb_detach_kernel_driver(dev, 1);
        libusb_claim_interface(dev, 1);

        device = dev;
        return 1;
    }
    return 0;
}

